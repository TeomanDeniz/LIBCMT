#ifndef KNR_STYLE_H
#	define KNR_STYLE_H 202503
#	ifdef __NOPROTO__
#		define KNR_STYLE
#	endif /* __NOPROTO__ */
#	ifndef KNR_STYLE
#		ifndef __STDC__
#			define KNR_STYLE
#		else
#			ifdef __STDC_VERSION__
#				if (__STDC_VERSION__ < 199409L) /* ... < C89 */
#					define KNR_STYLE
#				endif /* __STDC_VERSION__ >= 199409L */
#			else
#				define KNR_STYLE
#			endif /* __STDC_VERSION__ */
#		endif /* __STDC__ */
#	endif /* KNR_STYLE */
#endif /* KNR_STYLE_H */
